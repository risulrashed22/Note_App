extension AppDelegate: UNUserNotificationCenterDelegate{
    func userNotificationCenter(_ center: UNUserNotificationCenter, willPresent notification: UNNotification, withCompletionHandler completionHandler: @escaping (UNNotificationPresentationOptions) -> Void) {
        let id = notification.request.identifier // No need, but helps in case of error debugging
        print("Recieved in-app Notification with ID: \(id)")
        UNUserNotificationCenter.current().removeAllDeliveredNotifications() // Removes old notifications
        // Need to offer different kind of notifications apple has to offer
        completionHandler([.alert, .sound])
    }
